    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy(Request $request, Task $task)
    {
//        dd($task->all());
//        dd($request->all());
        $this->authorize('destroy', $task);

        $task->delete();

